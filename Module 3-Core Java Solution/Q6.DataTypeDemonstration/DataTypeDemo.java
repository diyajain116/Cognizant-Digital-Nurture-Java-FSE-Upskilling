public class DataTypeDemo {

    public static void main(String[] args) {

        // Declaring variables of different primitive data types
        int number = 100;
        float decimal1 = 12.5f;
        double decimal2 = 12345.6789;
        char letter = 'A';
        boolean isJavaFun = true;

        // Displaying values
        System.out.println("Integer value: " + number);
        System.out.println("Float value: " + decimal1);
        System.out.println("Double value: " + decimal2);
        System.out.println("Character value: " + letter);
        System.out.println("Boolean value: " + isJavaFun);
    }
}
